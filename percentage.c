//Program-1: To calculate percentage Ob/To * 100

//Directives and Libraries
#include <stdio.h>

//Main Function
int main (void)
{
    //Sequential - One-by-One
    
    float TotalMarks = 1000;
    
    float ObtainedMarks = 669;
    
    float Percentage = (ObtainedMarks/TotalMarks) * 100;
    
    printf("Your Percentage is: %f", Percentage );
    //%f - indicates that the value is Floating Point Integer

}


